//! WolfStack Pools — provision a federated tenant cluster of N VMs
//! from a single SP-side action.
//!
//! A "pool" represents an order: "give customer X a 3-VM WolfStack
//! cluster on backend Y". The driver fans out N VM creates with
//! cloud-init payloads that:
//!
//!   1. Pre-write the shared cluster_secret + per-VM join-token
//!      under /etc/wolfstack/ BEFORE setup.sh runs (setup.sh:260
//!      preserves existing config on re-runs, so this works).
//!   2. On the leader VM only, pre-write the federation token to
//!      /etc/wolfstack/federation_tokens.json so the SP can poll
//!      /api/federation/status as soon as the daemon is up.
//!   3. Run setup.sh → daemon up.
//!   4. On the leader, POST `/api/tenants/self-register` back to
//!      the SP with the bootstrap_token; SP validates against the
//!      pending pool and registers the tenant.
//!   5. On followers, POST `/api/cluster/bootstrap-add` to the
//!      leader with X-WolfStack-Secret = pool_secret + own
//!      join-token; leader joins them into its cluster.
//!
//! The bootstrap_token, pool_secret, federation_token, and
//! per-VM join_tokens are all generated by the SP and embedded in
//! cloud-init. The SP sees the full state machine — provisioning
//! → leader_up → followers_joining → live — through XO inventory
//! polling and the self-register callback.
//!
//! Driver layer:
//!
//!   * `Backend::Xo`     — `xo_driver` (this module): wraps existing
//!     `xo::XoClient::create_vm` with cloud-init.
//!   * `Backend::Proxmox`/`Backend::Native` — trait stubs for now;
//!     they return a clear `not_yet_implemented` error to the API.
//!     The user agreed to phased delivery (XO first, Proxmox next
//!     session, native after) so these are explicit not-yet, not
//!     half-implementations.

